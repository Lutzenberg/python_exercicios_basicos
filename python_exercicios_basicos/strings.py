# Concatenar três strings separadas por espaço
s1 = "ant"
s2 = "bat"
s3 = "cod"
res = s1 + " " + s2 + " " + s3
print(res)

# Repetir uma string usando multiplicação (com espaço)
res = (s1 + "") * 10
print(res)

# Repetir strings com espaços
res = (s1 + " ") * 1 + (s2 + " ") * 2 + (s3 + " ") * 3
print(res)
